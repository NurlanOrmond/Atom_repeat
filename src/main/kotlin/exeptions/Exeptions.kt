// если возникает error - то практически невозможно исправить эту ситуауию "налету" в процессе работы программы.
                            // дальнейшая работа программы прекращается

// exeption - есть шанс среагировать прямо во время выполнения программы, дальнейшее выполнение программы возможно.

// Что можно делать с исключениями?

fun main() {
    val a = 10
    val b = 0

    try {
        println(a/b)
    } catch (e: ArithmeticException) {
        println("Dividing by zero!")
    } catch (e: Exception) {
        println("Блоков catch может быть несколько")
    } finally {
        println("Завершающая операция. Необязательный блок. Он выполнится независимо есть exeption или нет." +
                "Примером м служить: ищем файл, если его - нет возникают исключения." +
                "В блоке finally мы создем его")
    }

    println("При этом программа не завершилась и продолжает работу!")
}

